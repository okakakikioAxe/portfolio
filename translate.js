function renderLang(lang) {
    document.querySelectorAll('[data-static-translate]').forEach(el => {
        const key = el.dataset.staticTranslate;
        const value = translations[key][lang];

        if (value) el.textContent = value;
    });

    document.querySelectorAll('[data-project-translate]').forEach(el => {
        const translateData = el.dataset.projectTranslate.split("-");
        const index = translateData[0];
        const key = translateData[1];
        const value = projects[index][key][lang];

        if (value) el.textContent = value;
    });

    document.querySelectorAll('[data-project-feature-translate]').forEach(el => {
        const translateData = el.dataset.projectFeatureTranslate.split("-");
        const projectIndex = translateData[0];
        const featureIndex = translateData[1];
        const key = translateData[2];
        const value = projects[projectIndex][key][lang][featureIndex];

        if (value) el.textContent = value;
    });

    document.querySelectorAll('[data-project-responsibility-translate]').forEach(el => {
        const translateData = el.dataset.projectResponsibilityTranslate.split("-");
        const projectIndex = translateData[0];
        const responsibilityIndex = translateData[1];
        const key = translateData[2];
        const value = projects[projectIndex][key][lang][responsibilityIndex];

        if (value) el.textContent = value;
    });

    document.querySelectorAll('[data-career-translate]').forEach(el => {
        const translateData = el.dataset.careerTranslate.split("-");
        const index = translateData[0];
        const key = translateData[1];
        const value = careers[index][key][lang];

        if (value) el.textContent = value;
    });
}

